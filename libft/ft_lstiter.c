#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *next_lst;

	while (lst)
	{
		next_lst = lst->next;
		(*f)(lst);
		lst = lst->next;
	}
}
